function getTempleImageQueries(temple) {
  return [...new Set([
    extractWikipediaQuery(temple),
    `${temple.displayName} ${temple.town} temple`,
    `${temple.canonicalName} ${temple.town}`,
    `${temple.deityName} ${temple.town}`,
    `${temple.displayName} temple`,
    temple.canonicalName
  ].filter(Boolean))];
}

function extractWikipediaQuery(temple) {
  const source = temple.sources.find((item) => item.url.includes("wikipedia.org/wiki/"));
  if (!source) {
    return null;
  }

  const titlePart = source.url.split("/wiki/")[1];
  if (!titlePart) {
    return null;
  }

  return decodeURIComponent(titlePart).replace(/_/g, " ");
}

async function searchWikipediaImage(query) {
  const params = new URLSearchParams({ q: query, limit: "3" });
  const response = await fetch(`https://en.wikipedia.org/w/rest.php/v1/search/page?${params.toString()}`);
  if (!response.ok) {
    return null;
  }

  const data = await response.json();
  const page = (data.pages || []).find((item) => item.thumbnail?.url);
  if (!page) {
    return null;
  }

  return {
    src: page.thumbnail.url,
    pageTitle: page.title,
    sourceUrl: `https://en.wikipedia.org/wiki/${encodeURIComponent(page.key || page.title.replace(/\s+/g, "_"))}`
  };
}

function isUsableCommonsPage(page) {
  const title = (page?.title || "").toLowerCase();
  if (!title.startsWith("file:")) {
    return false;
  }
  return !IMAGE_BLOCKLIST.some((term) => title.includes(term));
}

async function searchCommonsImage(query) {
  const params = new URLSearchParams({
    action: "query",
    format: "json",
    origin: "*",
    generator: "search",
    gsrsearch: query,
    gsrnamespace: "6",
    gsrlimit: "6",
    prop: "imageinfo",
    iiprop: "url",
    iiurlwidth: "1200"
  });

  const response = await fetch(`https://commons.wikimedia.org/w/api.php?${params.toString()}`);
  if (!response.ok) {
    return null;
  }

  const data = await response.json();
  const pages = Object.values(data.query?.pages || {}).filter(isUsableCommonsPage);
  const bestPage = pages.find((page) => page.imageinfo?.[0]?.thumburl || page.imageinfo?.[0]?.url);

  if (!bestPage) {
    return null;
  }

  const info = bestPage.imageinfo[0];
  return {
    src: info.thumburl || info.url,
    pageTitle: bestPage.title,
    sourceUrl: `https://commons.wikimedia.org/wiki/${encodeURIComponent(bestPage.title)}`
  };
}

async function fetchTempleImageRecord(temple) {
  if (Object.prototype.hasOwnProperty.call(imageCache, temple.slug)) {
    return imageCache[temple.slug];
  }

  if (imageRequests.has(temple.slug)) {
    return imageRequests.get(temple.slug);
  }

  const request = (async () => {
    for (const query of getTempleImageQueries(temple)) {
      try {
        const wikiResult = await searchWikipediaImage(query);
        if (wikiResult) {
          imageCache[temple.slug] = wikiResult;
          saveJson(STORAGE_KEYS.images, imageCache);
          return wikiResult;
        }
      } catch {
        continue;
      }
    }

    for (const query of getTempleImageQueries(temple)) {
      try {
        const result = await searchCommonsImage(query);
        if (result) {
          imageCache[temple.slug] = result;
          saveJson(STORAGE_KEYS.images, imageCache);
          return result;
        }
      } catch {
        continue;
      }
    }

    imageCache[temple.slug] = null;
    saveJson(STORAGE_KEYS.images, imageCache);
    return null;
  })();

  imageRequests.set(temple.slug, request);
  request.finally(() => imageRequests.delete(temple.slug));
  return request;
}

function buildTempleImageMarkup(temple, variant = "card") {
  const fallback = templePlaceholderSvg(temple, variant);
  return `
    <figure class="temple-photo temple-photo--${variant}" data-temple-image="${temple.slug}" data-variant="${variant}">
      <img src="${fallback}" alt="${templeDisplayName(temple)}" loading="lazy" />
      <figcaption data-photo-status>${variant === "profile" ? t("photoLoading") : templeRegionShortLabel(temple)}</figcaption>
    </figure>
  `;
}

function hydrateTempleImages(scope = document) {
  scope.querySelectorAll("[data-temple-image]").forEach((node) => {
    if (node.dataset.bound === "true") {
      return;
    }
    node.dataset.bound = "true";
    if (imageObserver) {
      imageObserver.observe(node);
    } else {
      loadTempleImageInto(node);
    }
  });
}

async function loadTempleImageInto(node) {
  const slug = node.getAttribute("data-temple-image");
  const temple = templeBySlug[slug];
  if (!temple) {
    return;
  }

  const img = node.querySelector("img");
  const caption = node.querySelector("[data-photo-status]");
  const variant = node.getAttribute("data-variant") || "card";

  if (!img || !caption) {
    return;
  }

  img.src = templePlaceholderSvg(temple, variant);
  caption.textContent = t("photoLoading");

  const imageRecord = await fetchTempleImageRecord(temple);
  if (!document.body.contains(node)) {
    return;
  }

  if (imageRecord?.src) {
    node.classList.add("has-image");
    img.src = imageRecord.src;
    img.alt = state.language === "ta" ? `${templeDisplayName(temple)} கோவில்` : `${temple.displayName} temple`;
    const sourceLabel = imageRecord.sourceUrl.includes("wikipedia.org")
      ? localizeSourceLabel("Wikipedia")
      : localizeSourceLabel("Wikimedia Commons");
    caption.innerHTML =
      variant === "profile"
        ? `${t("photoVia")} <a href="${imageRecord.sourceUrl}" target="_blank" rel="noreferrer">${sourceLabel}</a>`
        : t("cardPhotoLabel");
    return;
  }

  node.classList.add("is-fallback");
  caption.textContent = variant === "profile" ? t("photoFallback") : templeRegionShortLabel(temple);
}

function getNearbyTemples(temple) {
  const explicit = temple.nearby.map((slug) => templeBySlug[slug]).filter(Boolean);
  if (explicit.length > 0) {
    return explicit.slice(0, 4);
  }

  const sameRegion = temples.filter((item) => item.regionKey === temple.regionKey);
  const index = sameRegion.findIndex((item) => item.slug === temple.slug);
  return sameRegion.filter((_, itemIndex) => Math.abs(itemIndex - index) <= 2 && itemIndex !== index).slice(0, 4);
}

function matchesView(temple) {
  if (state.view === "expanded") {
    return temple.expanded;
  }
  if (state.view === "favorites") {
    return state.favorites.has(temple.slug);
  }
  if (state.view === "planner") {
    return state.planner.includes(temple.slug);
  }
  return true;
}

function getFilteredTemples() {
  return temples.filter((temple) => {
    const haystack = [
      temple.canonicalName,
      temple.displayName,
      temple.deityName,
      temple.goddessName,
      temple.town,
      temple.district,
      temple.regionLabel,
      temple.thevaramNote,
      localizeTempleText(temple.canonicalName),
      localizeTempleText(temple.displayName),
      localizeTempleText(temple.deityName),
      localizeTempleText(temple.goddessName),
      localizeTempleText(temple.town),
      localizeTempleText(temple.thevaramNote)
    ]
      .join(" ")
      .toLowerCase();

    const matchesQuery = state.query.length === 0 || haystack.includes(state.query);
    const matchesRegion = state.regionKey === "all" || temple.regionKey === state.regionKey;
    const matchesSaint =
      state.saint === "All" ||
      (state.saint === "Unknown" ? temple.nayanmars.length === 0 : temple.nayanmars.includes(state.saint));

    return matchesQuery && matchesRegion && matchesSaint && matchesView(temple);
  });
}

