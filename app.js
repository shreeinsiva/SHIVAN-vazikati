function matchTamilVowel(token, index) {
  return Object.keys(TAMIL_INDEPENDENT_VOWELS).find((key) => token.startsWith(key, index)) || null;
}

function matchTamilConsonant(token, index) {
  return TAMIL_CONSONANTS.find(([key]) => token.startsWith(key, index)) || null;
}

function transliterateTamilToken(token) {
  if (!token) {
    return "";
  }

  const lowerToken = token.toLowerCase();
  if (TAMIL_TOKEN_MAP[lowerToken]) {
    return TAMIL_TOKEN_MAP[lowerToken];
  }

  for (const [prefix, tamil] of TAMIL_PREFIX_MAP) {
    if (lowerToken.startsWith(prefix) && lowerToken.length > prefix.length) {
      return tamil + transliterateTamilToken(lowerToken.slice(prefix.length));
    }
  }

  let output = "";
  let index = 0;

  while (index < lowerToken.length) {
    const vowel = matchTamilVowel(lowerToken, index);
    if (vowel) {
      output += TAMIL_INDEPENDENT_VOWELS[vowel];
      index += vowel.length;
      continue;
    }

    const consonant = matchTamilConsonant(lowerToken, index);
    if (consonant) {
      const [key, base] = consonant;
      index += key.length;
      const vowelAfter = matchTamilVowel(lowerToken, index);
      if (vowelAfter) {
        output += base + TAMIL_VOWEL_SIGNS[vowelAfter];
        index += vowelAfter.length;
      } else {
        output += `${base}்`;
      }
      continue;
    }

    output += token[index];
    index += 1;
  }

  return output;
}

function localizeTempleText(value) {
  if (state.language !== "ta" || !value) {
    return value;
  }

  return String(value)
    .split(/(\s+|[(),/&-]+)/)
    .map((part) => {
      if (!/[A-Za-z]/.test(part)) {
        return part;
      }
      return transliterateTamilToken(part);
    })
    .join("")
    .replace(/\s+/g, " ")
    .trim();
}

